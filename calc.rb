#Learning puts, simple arithmetics, and float/int data types

puts 1+2 #statement that prints to screen whatever comes after it

#Calculations involving two float numbers

puts 1.0 + 2.0
puts 2.0 * 3.0
puts 5.0 - 8.0
puts 9.0 / 2.0

#Calculations involving two integer numbers

puts 1+2
puts 2*3
puts 5-8
puts 9/2 #This prints out 4 because of integer division

#Calculations involving a float and an integer

puts 5 / 2.0 #This prints out a float because at least one of the numbers used is a float

# Arithmetic is just syntactic sugar
# for calling a method on an object
1.+(3) #=> 4
10.* 5 #=> 50